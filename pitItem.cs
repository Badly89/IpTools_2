using System;

namespace OpTools
{
    public class pitItem
    {
        public bool consider;

        public bool Consider
        {
            get { return consider; }
            set { consider = value; }
        }

        public String name;

        public String Name
        {
            get { return name; }
            set { name = value; }
        }

        public String id;

        public String Id
        {
            get { return id; }
            set { id = value; }
        }

        public int count;

        public int Count
        {
            get { return count; }
            set { count = value; }
        }
    }
}
